const getInitialConsonant = (str: string) => {
  const CHO_HANGUL = [
    'ㄱ',
    'ㄲ',
    'ㄴ',
    'ㄷ',
    'ㄸ',
    'ㄹ',
    'ㅁ',
    'ㅂ',
    'ㅃ',
    'ㅅ',
    'ㅆ',
    'ㅇ',
    'ㅈ',
    'ㅉ',
    'ㅊ',
    'ㅋ',
    'ㅌ',
    'ㅍ',
    'ㅎ'
  ]
  const code = str.charCodeAt(0) - 44032
  if (code >= 0 && code <= 11171) {
    return CHO_HANGUL[Math.floor(code / 588)]
  }
  return /^[A-Za-z]/.test(str[0]) ? 'A-Z' : str[0]
}

